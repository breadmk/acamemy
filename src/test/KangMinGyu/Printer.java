package test.KangMinGyu;

//  Printer클래스에서 println()메소드를 선언하시오.
public class Printer {
	
//	field	[접근제어자][속성]데이터타입 변수명 = [초기값];
//	constructor	[접근제어자]클래스명([매개변수리스트]){}
	
//	method	[접근제어자][속성]리턴유형 메소드명([매개변수리스트]){}
	static void println(int num) {
		System.out.println(num);
	}
	static void println(boolean boo) {
		System.out.println(boo);
	}
	static void println(double dou) {
		System.out.println(dou);
	}
	static void println(String str) {
		System.out.println(str);
	}


}
